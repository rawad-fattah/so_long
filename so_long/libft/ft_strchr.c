#include "libft.h"

char	*ft_strchr(const char *s, int i)
{
	while (*s)
	{
		if (*s == (unsigned char)i)
			return ((char *)s);
		s++;
	}
	if (i == '\0')
		return ((char *)s + ft_strlen(s));
	return (0);
}
